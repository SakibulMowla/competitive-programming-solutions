#include<stdio.h>

int main()
{
    int a,b,c,d,i,n,sum;
    scanf("%d",&n);
    for(i=0;i<n;i++)
    {
        scanf("%d %d %d %d",&a,&b,&c,&d);
        sum=0;
        sum+=((a/1000)*2)/10;
        sum+=((a/1000)*2)%10;
        sum+=((b/1000)*2)/10;
        sum+=((b/1000)*2)%10;
        sum+=((c/1000)*2)/10;
        sum+=((c/1000)*2)%10;
        sum+=((d/1000)*2)/10;
        sum+=((d/1000)*2)%10;
        sum+=(((a%100)/10)*2)/10;
        sum+=(((a%100)/10)*2)%10;
        sum+=(((b%100)/10)*2)/10;
        sum+=(((b%100)/10)*2)%10;
        sum+=(((c%100)/10)*2)/10;
        sum+=(((c%100)/10)*2)%10;
        sum+=(((d%100)/10)*2)/10;
        sum+=(((d%100)/10)*2)%10;
        sum+=((a%1000)/100);
        sum+=((b%1000)/100);
        sum+=((c%1000)/100);
        sum+=((d%1000)/100);
        sum+=a%10;
        sum+=b%10;
        sum+=c%10;
        sum+=d%10;
        if(sum%10==0)
            printf("Valid\n");
        else
            printf("Invalid\n");
    }
    return 0;
}
