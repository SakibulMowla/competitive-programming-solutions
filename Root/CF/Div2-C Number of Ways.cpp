const ll inf = 1;
const ll mod = 1LL;

#define SZ 5*100010

ll arr[SZ];
ll dp1[SZ];
ll dp2[SZ];
map <ll, int> cnt;

int main() {
	double t = clock();
	
    #if defined JESI
        //freopen("1.txt", "r", stdin);
        //freopen("2.txt", "w", stdout);
    #endif
	
	int n, i;
	ll ans;

    while(scanf("%d",&n) == 1) {
		clr(cnt);
		dp1[0] = dp2[n+1] = 0LL;
		
		for(i = 1; i <= n; i++) 
			scanf("%I64d",&arr[i]);
		
		for(i = 1; i <= n; i++) 
			dp1[i] = arr[i] + dp1[i-1];

		for(i = n; i >= 1; i--) {
			dp2[i] = arr[i] + dp2[i+1];
			if(i >= 3) cnt[ dp2[i] ]++;
		}
		
		ans = 0LL;
		
		for(i = 3; i <=n; i++) {
			if(2LL * dp1[i-2] == dp2[i-1]) {
				ans += cnt[ dp1[i-2] ];
			}
			cnt[ dp2[i] ]--;
		}
		
		printf("%I64d\n",ans);
		
		#ifdef JESI
			printf("time = %lf seconds\n",(clock()-t)/CLOCKS_PER_SEC);
		#endif
    }

    return 0;
}








